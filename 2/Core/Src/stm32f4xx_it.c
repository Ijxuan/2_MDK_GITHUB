/**
  * @brief This function handles USART6 global interrupt.
  */
void USART6_IRQHandler(void)
{
  /* USER CODE BEGIN USART6_IRQn 0 */
static uint8_t rxBuf[30]; //接收缓冲区
	static char debugopen; //接收缓冲区

	static uint8_t rxCnt; //接收计数器
	//收到一个字节进入RXNE
	if(__HAL_UART_GET_FLAG(&huart6,UART_FLAG_RXNE)!=RESET)
	{
		//保存收到的一个字节
		rxBuf[rxCnt++]=huart6.Instance->DR;
		debugopen=huart6.Instance->DR;
	}
	//进入空闲中断，一帧结束，进行解析
	else if(__HAL_UART_GET_FLAG(&huart6,UART_FLAG_IDLE)!=RESET)
	{
		
		__HAL_UART_CLEAR_IDLEFLAG(&huart6); //清除标志位
		Debug_DecodeFrame(rxBuf); //解析数据
		rxCnt=0; //计数器归零
		if(debugopen=='0')
		{
			minibalencedebug=0;
		}
			else if(debugopen=='1')
			{
				minibalencedebug=1;
			}
	}
  /* USER CODE END USART6_IRQn 0 */
  HAL_UART_IRQHandler(&huart6);
  /* USER CODE BEGIN USART6_IRQn 1 */

  /* USER CODE END USART6_IRQn 1 */
}

